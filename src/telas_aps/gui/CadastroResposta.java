    public List<Resposta> getRespostas() {
        if(respostas == null) respostas = new ArrayList<>();
        return respostas;
    }

    public void setRespostas(List<Resposta> respostas) {
        this.respostas = respostas;
    }

    public CadastroFase getFormFase() {
        return formFase;
    }

    public void setFormFase(CadastroFase formFase) {
         this.formFase = formFase;
    }

    private Resposta getResposta() throws Exception{
        try{
            Resposta r = new Resposta();
            
            r.setMsgResposta(txtMsgResp.getText());
            r.setTextResposta(txtResp.getText());
            r.setPontoResposta(Integer.valueOf(txtPonto.getText()));
            
            return r;
        }catch(Exception e){
            throw new Exception("Erro ao obter a reposta causadado por: "+e.getMessage());
        }
    }
    /*
    -----------------------------------------------------------------------------------
    UTEIS PARA A TELA
    -----------------------------------------------------------------------------------
    */
    private void checkValores(){
        if(txtPonto.getText().length()<=0)
            throw new ValorInvalidoException("Informe o ponto da questão");
    }

    private void showMsgErro(String titulo, String msg){
         JOptionPane.showMessageDialog( // Caixa de mensagem
		null, // Janela da aplicação (opcional, pode ser null)
		msg, // Mensagem
		titulo, // Título da caixa de mensagem
		JOptionPane.ERROR_MESSAGE // Ícone da caixa de mensagem
	);
    }
    
    private ListSelectionListener tbRespostaListner = new ListSelectionListener() {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if(tbResposta.getSelectedRow()>-1){
                Resposta r = respostaModel.getRowByIndex(tbResposta.getSelectedRow());
                
                txtResp.setText(r.getTextResposta());
                txtMsgResp.setText(r.getMsgResposta());
                txtPonto.setText(String.valueOf(r.getPontoResposta()));
            }
        }
    };
     
