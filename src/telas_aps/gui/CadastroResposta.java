    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       try{
           getFormFase().setRespostas(respostaModel.getRespostas());
           dispose();
       }catch(Exception e){
           e.printStackTrace();
           showMsgErro("Erro ao salvar", "Causado por: "+e.getMessage());
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try{
            if(tbResposta.getSelectedRow()<0)
                throw new Exception("Nenhuma reposta selecionada");
            
            respostaModel.update(tbResposta.getSelectedRow(), getResposta());
            
        }catch(Exception e){
            e.printStackTrace();
            showMsgErro("Erro ao remover", "Causado por: "+e.getMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            checkValores();
            
            respostaModel.addRow(getResposta());
            
        }catch(ValorInvalidoException e){
            showMsgErro("Erro ao adicionar", e.getMessage());
        }catch(Exception e){
            showMsgErro("Erro ao adicionar", "Causado Por: "+e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            if(tbResposta.getSelectedRow()<0)
                throw new Exception("Nenhuma reposta selecionada");
            
            respostaModel.removeRow(tbResposta.getSelectedRow());
            
        }catch(Exception e){
            e.printStackTrace();
            showMsgErro("Erro ao remover", "Causado por: "+e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    
